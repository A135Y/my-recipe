// Boston Cream Brioche Doughnuts Recipe
// Yields 12 servings

// Ingredients
// For the dough: 1 cup whole milk
// 1 (¼-ounce) envelope active dry yeast (about 2¼ tsp.)
// ¼ cup granulated sugar
// 4 cups all-purpose flour, plus more for dusting
// 1 teaspoon kosher salt
// 3 large eggs, room temperature
// 10 tablespoons unsalted butter, cut into pieces, room temperature, plus more for greasing
// For the pastry cream: 1 cup whole milk
// 1 vanilla bean, split lengthwise
// ¼ cup granulated sugar
// 3 large egg yolks
// 2 tablespoons cornstarch
// 1 tablespoon unsalted butter
// For the chocolate glaze and assembly: 4 ounces semisweet chocolate, chopped
// ½ cup heavy cream
// 1 tablespoon light corn syrup
// Directions
// Make the dough:
// Heat milk in a small saucepan over medium or in a microwave until an instant-read thermometer registers 110°–115°. Transfer milk to a 2-cup measuring glass; stir in yeast and 1 tsp. sugar. Let sit until foamy, about 5 minutes.
// Whisk together flour, salt, and remaining ¼ cup sugar in the bowl of a stand mixer fitted with the paddle attachment. Add eggs and yeast mixture and mix on low speed until nearly combined, about 30 seconds. Switch to the dough hook and add 10 Tbsp. butter; mix until butter is almost completely incorporated, about 1 minute. Increase speed to medium and beat until dough is soft and slightly sticky, about 8 minutes.
// Transfer dough to a lightly floured surface and knead a few times until smooth. Shape into a ball. Butter a large bowl and add dough, turning to coat. Cover with plastic wrap and let rise in a warm, draft-free place until doubled in size,
// 45–60 minutes.
// Make the pastry cream:
// Heat milk in a small saucepan over medium or in a microwave until an instant-read thermometer registers 110°–115°. Scrape in seeds from vanilla bean; add bean. Cover and let steep 10 minutes.
// Whisk sugar, egg yolks, and cornstarch in a medium bowl until smooth. Gradually whisk in milk. Return mixture to saucepan and cook over medium heat, whisking constantly, until thickened and whisk leaves a trail in pastry cream, about 2 minutes. Remove from heat and whisk in butter. Strain through a fine-mesh sieve into a medium bowl. Press plastic wrap directly onto surface and chill until cold, at least 2 hours.
// Make the chocolate glaze and assemble:
// Place chocolate in a medium bowl. Heat cream and corn syrup in a small saucepan over medium until just beginning to simmer; pour over chocolate. Let sit 5 minutes, then whisk until smooth. Let cool until slightly thickened, about 15 minutes.
// Punch down dough and transfer to a lightly floured surface. Roll out to a 16x12" rectangle. Using a 3" round cutter, cut out 12 rounds. Transfer to a parchment-lined baking sheet, spacing 2" apart. Cover with plastic wrap and let rise in a warm, draft-free place until doubled in size, 45–60 minutes.
// Pour oil into a large heavy pot fitted with thermometer to come halfway up sides. Heat over medium-high until thermometer registers 350°. Working in batches, fry doughnuts, turning occasionally, until deep golden brown and cooked through, about 3 minutes per batch. Transfer to a wire rack set inside a rimmed baking sheet and let cool slightly.
// Using a paring knife, poke a hole in the side of each doughnut. Transfer pastry cream to a piping bag fitted with a small round tip. Pipe pastry cream into doughnuts until filled. Dip tops of doughnuts into chocolate glaze and return to rack. Let sit until glaze is set, about 30 minutes.

