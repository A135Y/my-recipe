// Cinnamon Rolls Recipe
// Yields 12 servings

// Ingredients
// For the dough: 1 cup whole milk
// 1 (¼-ounce) envelope active dry yeast (about 2¼ tsp.)
// ¼ cup granulated sugar
// 4 cups all-purpose flour, plus more for dusting
// 1 teaspoon kosher salt
// 3 large eggs, room temperature
// 10 tablespoons unsalted butter, cut into pieces, room temperature, plus more for greasing
// For the filling and assembly:
// ¾ cup (packed) light brown sugar
// ¼ cup granulated sugar
// 2 tablespoons ground cinnamon
// ½ teaspoon kosher salt
// 6 tablespoons unsalted butter, room temperature
// 1 large egg, beaten to blend
// For the glaze: 4 ounces cream cheese, room temperature
// 4 tablespoons unsalted butter, room temperature
// 1 cup powdered sugar
// ½ teaspoon vanilla extract
// Pinch of kosher salt
// Directions
// Make the dough:
// Heat milk in a small saucepan over medium or in a microwave until an instant-read thermometer registers 110°–115°. Transfer milk to a 2-cup measuring glass; stir in yeast and 1 tsp. sugar. Let sit until foamy, about 5 minutes.
// Whisk together flour, salt, and remaining ¼ cup sugar in the bowl of a stand mixer fitted with the paddle attachment. Add eggs and yeast mixture and mix on low speed until nearly combined, about 30 seconds. Switch to the dough hook and add 10 Tbsp. butter; mix until butter is almost completely incorporated, about 1 minute. Increase speed to medium and beat until dough is soft and slightly sticky, about 8 minutes.
// Transfer dough to a lightly floured surface and knead a few times until smooth. Shape into a ball. Butter a large bowl and add dough, turning to coat. Cover with plastic wrap and let rise in a warm, draft-free place until doubled in size,
// 45–60 minutes.
// Make the filling and assemble:
// Whisk brown sugar, granulated sugar, cinnamon, and salt in a medium bowl.
// Punch down dough and transfer to a lightly floured surface. Roll out to a 16x12" rectangle. Spread butter over dough, leaving a ½" border. Sprinkle cinnamon mixture over. Starting at a long end, roll up dough, jelly-roll style, into a log. Pinch seam to seal. Cut crosswise into 12 pieces.
// Lightly coat a 9x13" baking dish with butter. Arrange pieces, cut side up, in prepared dish. Cover with plastic wrap and let rise in a warm, draft-free place until doubled in size, 45–60 minutes.
// Preheat oven to 350°. Brush dough with egg. Bake until golden brown and rolls are baked through,
// 25–30 minutes.
// Make the glaze:
// Using an electric mixer on medium-high speed, beat cream cheese and butter in a medium bowl until light and fluffy, about 2 minutes. Add powdered sugar, vanilla, and salt and beat on low speed until incorporated. Increase speed to medium-high and beat until frosting is light and fluffy, about 2 minutes.
// Spread glaze over warm rolls. Serve warm or at room temperature.


