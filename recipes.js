// 1 bay leaf
// 1 pound rigatoni
// 2 tablespoons unsalted butter
// 1 ounce finely grated Parmesan, plus more for serving
// Directions
// Heat 2 Tbsp. oil in a large Dutch oven or other heavy pot over medium-high. Add beef, season with salt, and cook, breaking up with a wooden spoon, until browned and cooked through,
