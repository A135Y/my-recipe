// Peach Cobbler Recipe
// Yields 8 servings

// Ingredients
// For the filling: 2½ pounds ripe peaches (about 6), halved, pitted, cut into ½" wedges
// ½ cup (packed) light brown sugar
// ¼ cup granulated sugar
// 2 tablespoons cornstarch
// 1 tablespoon fresh lemon juice
// 1 teaspoon vanilla extract
// ½ teaspoon kosher salt
// For the topping: 1 cup all-purpose flour
// ½ cup granulated sugar
// 1 teaspoon baking powder
// ½ teaspoon kosher salt
// 6 tablespoons chilled unsalted butter, cut into pieces
// ½ cup buttermilk
// 1 tablespoon raw sugar
// Directions
// Make the filling:
// Preheat oven to 350°. Toss peaches, brown sugar, granulated sugar, cornstarch, lemon juice, vanilla, and salt in a 13x9" baking dish to combine. Bake, tossing once, until juices are bubbling, 35–40 minutes.
// Meanwhile, make the topping:
// Whisk flour, granulated sugar, baking powder, and salt in a medium bowl. Using your fingers, work butter into dry ingredients until no dry spots remain and mixture holds together when squeezed. Add buttermilk and mix gently with a fork, then knead in bowl until dough forms.
// Increase oven temperature to 375°. Tear topping into ½" pieces and scatter over filling. Sprinkle with raw sugar. Bake cobbler until topping is golden brown and juices are thick and bubbling, 30–35 minutes. Let cool 1 hour before serving.


